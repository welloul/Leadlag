#[cfg(test)]
mod tests {
    use super::*;
    use crate::eal::Symbol;

    fn make_tick(venue: VenueId, price: f64, size: f64, ts_ns: u64) -> Tick {
        Tick {
            venue,
            symbol: Symbol::new("BTC"),
            price,
            size,
            exchange_ts_ns: ts_ns,
            local_ts_ns: ts_ns,
        }
    }

    #[test]
    fn test_midprice_tracker_basic() {
        let mut tracker = MidpriceTracker::new(5_000_000);

        let delta = tracker.update(&make_tick(VenueId::EXCHANGE_A, 100.0, 1.0, 0));
        assert!(delta.is_none());
        assert!(tracker.initialized);

        let delta = tracker.update(&make_tick(VenueId::EXCHANGE_A, 100.1, 1.0, 2_000_000));
        assert!(delta.is_none());

        // Warmup delta (skipped)
        let delta = tracker.update(&make_tick(VenueId::EXCHANGE_A, 100.5, 1.0, 6_000_000));
        assert!(delta.is_none(), "First delta should be warmup");
        assert!(tracker.warmed_up);

        // Real delta
        let delta = tracker.update(&make_tick(VenueId::EXCHANGE_A, 100.6, 1.0, 12_000_000));
        assert!(delta.is_some());
        assert!((delta.unwrap() - 9.95).abs() < 1.0);
    }

    #[test]
    fn test_impulse_detector_basic() {
        let mut detector = ImpulseDetector::new(
            5_000_000,
            5.0,
            1.5,
            0.001,
            10_000_000,
            400_000_000, // 400ms freshness
        );

        // Init both trackers
        detector.process_tick(&make_tick(VenueId::EXCHANGE_A, 100.0, 1.0, 0));
        detector.process_tick(&make_tick(VenueId::EXCHANGE_B, 100.0, 1.0, 0));

        // Warmup
        detector.process_tick(&make_tick(VenueId::EXCHANGE_A, 100.0, 1.0, 6_000_000));
        detector.process_tick(&make_tick(VenueId::EXCHANGE_B, 100.0, 1.0, 6_000_000));

        // A moves 10bps, B flat
        let signal = detector.process_tick(&make_tick(VenueId::EXCHANGE_A, 100.1, 1.0, 12_000_000));
        assert!(
            signal.is_some(),
            "Impulse should fire: A moved 10bps, B flat"
        );
        let sig = signal.unwrap();
        assert_eq!(sig.side, OrderSide::Buy);
        assert_eq!(sig.target_venue, VenueId::EXCHANGE_B);
    }

    #[test]
    fn test_freshness_gate_rejects_stale() {
        let mut detector =
            ImpulseDetector::new(5_000_000, 5.0, 1.5, 0.001, 10_000_000, 400_000_000);

        // Init and warmup A only
        detector.process_tick(&make_tick(VenueId::EXCHANGE_A, 100.0, 1.0, 0));
        detector.process_tick(&make_tick(VenueId::EXCHANGE_A, 100.0, 1.0, 6_000_000));

        // B not initialized — no impulse
        let signal = detector.process_tick(&make_tick(VenueId::EXCHANGE_A, 100.5, 1.0, 12_000_000));
        assert!(signal.is_none(), "No impulse: B not initialized");
    }

    #[test]
    fn test_impulse_detector_filters_small_trades() {
        let mut detector =
            ImpulseDetector::new(5_000_000, 5.0, 1.5, 0.001, 10_000_000, 400_000_000);
        let signal = detector.process_tick(&make_tick(VenueId::EXCHANGE_A, 100.1, 0.0001, 0));
        assert!(signal.is_none());
    }

    #[test]
    fn test_local_timestamp_tracking() {
        let mut tracker = MidpriceTracker::new(5_000_000);
        assert_eq!(tracker.last_local_update_ns, 0);

        tracker.update(&make_tick(VenueId::EXCHANGE_A, 100.0, 1.0, 0));
        assert!(
            tracker.last_local_update_ns > 0,
            "Local timestamp should be set"
        );
    }
}
