//! Trade impulse detector for microstructure alpha.
//!
//! Detects fast price moves on one exchange while the other lags.
//! Pure event-driven signal - no correlation needed.
//!
//! # Algorithm
//! 1. Track midprice changes on both exchanges
//! 2. Detect when one exchange moves > threshold_bps
//! 3. Check if other exchange hasn't reacted (< lag_threshold_bps)
//! 4. Generate signal to trade the laggard

use crate::eal::{OrderSide, Symbol, Tick, VenueId};

/// Midprice tracker for impulse detection
///
/// Tracks price changes over a sliding window to detect fast moves.
/// All operations are O(1) with no heap allocation.
struct MidpriceTracker {
    /// Current midprice (from last tick)
    current_mid: Option<f64>,
    /// Midprice from N ms ago
    prev_mid: Option<f64>,
    /// Timestamp of prev_mid update
    prev_timestamp_ns: u64,
    /// Window size in nanoseconds
    window_ns: u64,
}

impl MidpriceTracker {
    /// Create a new midprice tracker
    #[inline(always)]
    fn new(window_ns: u64) -> Self {
        Self {
            current_mid: None,
            prev_mid: None,
            prev_timestamp_ns: 0,
            window_ns,
        }
    }

    /// Update with new tick, returns delta if window elapsed
    ///
    /// Returns Some(delta_bps) if enough time has elapsed since last update.
    /// Delta is in basis points (1 bps = 0.01%).
    #[inline(always)]
    fn update(&mut self, tick: &Tick) -> Option<f64> {
        let current_ns = tick.exchange_ts_ns;
        let price = tick.price;

        // Update current mid
        self.current_mid = Some(price);

        // Check if window has elapsed
        if let Some(prev) = self.prev_mid {
            if current_ns - self.prev_timestamp_ns >= self.window_ns {
                let delta = (price - prev) / prev * 10000.0; // Convert to bps
                self.prev_mid = Some(price);
                self.prev_timestamp_ns = current_ns;
                return Some(delta);
            }
        } else {
            // First tick, initialize prev
            self.prev_mid = Some(price);
            self.prev_timestamp_ns = current_ns;
        }

        None
    }

    /// Get current midprice
    #[inline(always)]
    fn current_mid(&self) -> Option<f64> {
        self.current_mid
    }
}

/// Impulse signal generated by trade impulse detector
#[derive(Debug, Clone)]
pub struct ImpulseSignal {
    /// Direction of the trade
    pub side: OrderSide,
    /// Target venue (the laggard)
    pub target_venue: VenueId,
    /// Symbol to trade
    pub symbol: Symbol,
    /// Impulse magnitude in basis points
    pub impulse_magnitude_bps: f64,
    /// Timestamp when signal was generated
    pub timestamp_ns: u64,
}

/// Trade impulse detector
///
/// Detects fast price moves on one exchange while the other remains flat.
/// This is pure alpha - no correlation needed.
pub struct ImpulseDetector {
    /// Midprice trackers for each venue
    tracker_a: MidpriceTracker,
    tracker_b: MidpriceTracker,
    /// Threshold in bps to detect impulse
    impulse_threshold_bps: f64,
    /// Max move on other exchange to consider it "lagging"
    lag_threshold_bps: f64,
    /// Minimum trade size to avoid fake impulses
    min_trade_size: f64,
    /// Last impulse timestamp for timeout
    last_impulse_ns: Option<u64>,
    /// Signal timeout in nanoseconds
    signal_timeout_ns: u64,
}

impl ImpulseDetector {
    /// Create a new impulse detector
    ///
    /// # Arguments
    /// * `window_ns` - Lookback window in nanoseconds (e.g., 5_000_000 for 5ms)
    /// * `impulse_threshold_bps` - Price move threshold in bps (e.g., 5)
    /// * `lag_threshold_bps` - Max move on other exchange (e.g., 1.5)
    /// * `min_trade_size` - Minimum trade size to filter fake impulses
    /// * `signal_timeout_ns` - Signal timeout in nanoseconds
    pub fn new(
        window_ns: u64,
        impulse_threshold_bps: f64,
        lag_threshold_bps: f64,
        min_trade_size: f64,
        signal_timeout_ns: u64,
    ) -> Self {
        Self {
            tracker_a: MidpriceTracker::new(window_ns),
            tracker_b: MidpriceTracker::new(window_ns),
            impulse_threshold_bps,
            lag_threshold_bps,
            min_trade_size,
            last_impulse_ns: None,
            signal_timeout_ns,
        }
    }

    /// Process tick and detect impulse
    ///
    /// Returns signal if:
    /// 1. One exchange moves > impulse_threshold_bps
    /// 2. Other exchange hasn't reacted (< lag_threshold_bps)
    /// 3. Trade size >= min_trade_size
    /// 4. Signal hasn't timed out
    pub fn process_tick(&mut self, tick: &Tick) -> Option<ImpulseSignal> {
        // Filter small trades
        if tick.size < self.min_trade_size {
            return None;
        }

        let venue = tick.venue;
        let symbol = tick.symbol.clone();
        let timestamp_ns = tick.exchange_ts_ns;

        // Update trackers and get deltas
        let delta_a = self.tracker_a.update(tick);
        let delta_b = self.tracker_b.update(tick);

        // Check for impulse on exchange A
        if venue == VenueId::EXCHANGE_A {
            if let Some(delta) = delta_a {
                if delta.abs() > self.impulse_threshold_bps {
                    // Check if B is lagging
                    if let Some(delta_b) = delta_b {
                        if delta_b.abs() < self.lag_threshold_bps {
                            // Impulse detected!
                            self.last_impulse_ns = Some(timestamp_ns);
                            let side = if delta > 0.0 {
                                OrderSide::Buy
                            } else {
                                OrderSide::Sell
                            };
                            return Some(ImpulseSignal {
                                side,
                                target_venue: VenueId::EXCHANGE_B,
                                symbol,
                                impulse_magnitude_bps: delta,
                                timestamp_ns,
                            });
                        }
                    }
                }
            }
        }

        // Check for impulse on exchange B
        if venue == VenueId::EXCHANGE_B {
            if let Some(delta) = delta_b {
                if delta.abs() > self.impulse_threshold_bps {
                    // Check if A is lagging
                    if let Some(delta_a) = delta_a {
                        if delta_a.abs() < self.lag_threshold_bps {
                            // Impulse detected!
                            self.last_impulse_ns = Some(timestamp_ns);
                            let side = if delta > 0.0 {
                                OrderSide::Buy
                            } else {
                                OrderSide::Sell
                            };
                            return Some(ImpulseSignal {
                                side,
                                target_venue: VenueId::EXCHANGE_A,
                                symbol,
                                impulse_magnitude_bps: delta,
                                timestamp_ns,
                            });
                        }
                    }
                }
            }
        }

        None
    }

    /// Check if signal has timed out
    pub fn is_signal_expired(&self, current_ns: u64) -> bool {
        if let Some(last) = self.last_impulse_ns {
            current_ns - last > self.signal_timeout_ns
        } else {
            false
        }
    }

    /// Get current midprice for a venue
    pub fn current_mid(&self, venue: VenueId) -> Option<f64> {
        match venue {
            VenueId::EXCHANGE_A => self.tracker_a.current_mid(),
            VenueId::EXCHANGE_B => self.tracker_b.current_mid(),
            _ => None,
        }
    }
}

